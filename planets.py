def weight_on_planets():
  Myweight=float(input("What do you weigh on earth? "))
  WeightonMars=Myweight*0.38
  WeightonJupiter=Myweight*2.34
  print("\nOn Mars you would weigh", WeightonMars, "pounds.\nOn Jupiter you would weigh", WeightonJupiter, "pounds.")
if __name__ == '__main__':
   weight_on_planets()
